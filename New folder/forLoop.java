package day6;

public class forLoop {
	public static void main(String[] args) {
		
		int i=1;
		for (i=1;i<=10;i++) {
			System.out.println("iteration :"+ i);
		}
		
		System.out.println("               ");
		
		for (i=10;i>=1;i--) {
			System.out.println("iteration :"+ i);
		}
	}
}
